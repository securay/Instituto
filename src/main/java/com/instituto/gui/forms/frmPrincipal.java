    private void mnuGenerarPagosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuGenerarPagosActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(this, "Â¿Esta seguro de generar los pagos?", "Pagos", JOptionPane.YES_NO_OPTION, 
                JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
            double monto;
            String s= JOptionPane.showInputDialog(this, "Ingrese el monto a cobrar", 
                    "Pagos", JOptionPane.INFORMATION_MESSAGE);
            if(s != null) {
                try {
                    monto= Double.parseDouble(s);
                    GenerarPagos gp= new GenerarPagos((DataSource)context.getBean("dataSource"), monto);
                    gp.generar();
                    JOptionPane.showMessageDialog(this, "Se generaron los pagos con exito", "Pagos", 
                        JOptionPane.INFORMATION_MESSAGE);
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "Pagos", 
                        JOptionPane.ERROR_MESSAGE);
                    mnuGenerarPagosActionPerformed(evt);
                } catch (DataAccessException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "Pagos", 
                        JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Se cancelo la operacion", "Pagos", 
                        JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Se cancelo la operacion", "Pagos", 
                        JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_mnuGenerarPagosActionPerformed

