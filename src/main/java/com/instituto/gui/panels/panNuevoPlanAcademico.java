    private void loadEspecialidades() {
        if(es == null) {
            es= ce.listarEspecialidades();
        }
        cmbEspecialidad.removeAllItems();
        for (Especialidad e : es) {
            cmbEspecialidad.addItem(e.getNombre());
        }
        loadCursos();
    }
    
    private void loadCursos() {
        cmbNombreCurso.removeAllItems();
        if(cmbEspecialidad.getSelectedIndex() >= 0) {
            cu= cc.listarNombreCursos(
                    es.get(cmbEspecialidad.getSelectedIndex()).getIdEspecialidad());
            for (NombreCurso nc : cu) {
                cmbNombreCurso.addItem(nc.getDescripcion());
            }
        }
    }
    
    private boolean isRegistred(Long id) {
        for (DetallePlanAcademico d : dpa) {
            if(d.getIdNombreCurso().getIdNombreCursos() == id)
                return true;
        }
        return false;
    }
    
    public void limpiar() {
        txtNombre.setText("");
        dtm.setRowCount(0);
        cmbEspecialidad.setSelectedIndex(0);
        cmbCiclo.setSelectedIndex(0);
    }

