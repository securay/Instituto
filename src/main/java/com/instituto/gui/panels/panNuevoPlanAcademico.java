    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if(cmbNombreCurso.getSelectedIndex() >= 0) {
            NombreCurso nc= cu.get(cmbNombreCurso.getSelectedIndex());
            if(!isRegistred(nc.getIdNombreCursos())) {
                DetallePlanAcademico d= new DetallePlanAcademico();
                d.setIdCiclo(cmbCiclo.getSelectedItem().toString());
                d.setIdNombreCurso(nc);
                dpa.add(d);
                dtm.addRow(new Object[]{nc.getIdNombreCursos(), nc.getDescripcion(), 
                    cmbCiclo.getSelectedItem()});
            } else {
                JOptionPane.showMessageDialog(this, "El curso ya esta incluido en "
                        + "el plan academico", "Plan Academico", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un curso", 
                    "Plan Academico", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        if(txtNombre.getText().length() > 0 && cmbEspecialidad.getSelectedIndex() >= 0) {
            if(dtm.getRowCount() > 0) {
                PlanAcademico pa= new PlanAcademico();
                pa.setDescripcion(txtNombre.getText());
                pa.setIdEspecialidad(es.get(cmbEspecialidad.getSelectedIndex()));
                cpa.insertarPlanAcademico(pa);
                for (DetallePlanAcademico d : dpa) {
                    d.setIdPlanAcademico(pa);
                    cdp.insertarDetallePlanAcademico(d);
                }
                JOptionPane.showMessageDialog(this, "Se registro con exito el Plan Academico", 
                    "Plan Academico", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            } else {
                JOptionPane.showMessageDialog(this, "Debe agregar cursos al Plan Academico", 
                    "Plan Academico", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe ingresar los datos generales", 
                    "Plan Academico", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnOkActionPerformed

