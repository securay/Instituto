    public void registrarAdministrativo() {
        Profesor p= new Profesor();
        boolean res= true;
        if(txtCodigo.getText().length() > 0) {
            p.setIdProfesor(txtCodigo.getText());
            p.setContrasena(txtCodigo.getText());
        } else res= false;
        if(txtApellidoM.getText().length() > 0) {
            p.setApellidoMaterno(txtApellidoM.getText());
        } else res= false;
        if(txtApellidoP.getText().length() > 0) {
            p.setApellidoPaterno(txtApellidoP.getText());
        } else res= false;
        
        if(txtDireccion.getText().length() > 0) {
            p.setDireccion(txtDireccion.getText());
        } else res= false;
        if(txtDNI.getText().length() == 8) {
            p.setDni(txtDNI.getText());
        } else res= false;
        if(txtNombre.getText().length() > 0) {
            p.setNombres(txtNombre.getText());
        } else res= false;
        p.setCelular(txtCelular.getText());
        p.setEmail(txtEmail.getText());
        p.setTelefono(txtTelefono.getText());
        p.setActivo(true);
        if(res) {
            Exception ex= cp.insertarProfesor(p);
            if(ex == null) {
                JOptionPane.showMessageDialog(this, "Se registro con exito el profesor", 
                    "Alumno", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            } else
                JOptionPane.showMessageDialog(this, ex.getMessage(), 
                    "Alumno", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No se registraron los campos obligatorios", 
                    "Alumno", JOptionPane.INFORMATION_MESSAGE);
            Border b= BorderFactory.createEtchedBorder(Color.red, Color.red);
            txtCodigo.setBorder(b);
            txtApellidoM.setBorder(b);
            txtApellidoP.setBorder(b);
            txtDNI.setBorder(b);
            txtDireccion.setBorder(b);
            txtNombre.setBorder(b);
        }
    }

