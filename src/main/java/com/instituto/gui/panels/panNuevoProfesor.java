    private void llenarDatos(Profesor p) {
        if(p != null) {
            txtApellidoM.setText(p.getApellidoMaterno());
            txtApellidoP.setText(p.getApellidoPaterno());
            txtCelular.setText(p.getCelular());
            txtCodigo.setText(p.getIdProfesor());
            txtDNI.setText(p.getDni());
            txtDireccion.setText(p.getDireccion());
            txtEmail.setText(p.getEmail());
            txtNombre.setText(p.getNombres());
            txtTelefono.setText(p.getTelefono());
            txtCodigo.setEditable(false);
        }
    }
    
